# +, -, *, /, %, **, //:
a = 10
b = 3
print(a+b, a-b, a*b, a/b, a%b, a**b, a//b)

# ==, !=, >, >=, <, <=:
print(10 == 5)
print(10 != 5)
print(10 > 5)
print(10 < 5)
print(10 >= 5)
print(10 <= 10)

# or,and,not
print("Comparison:")
print((5 == 5) or (5 > 10))
print((5 == 5) and (5 > 10))
print(not ((5 == 5) or (5 > 10)))