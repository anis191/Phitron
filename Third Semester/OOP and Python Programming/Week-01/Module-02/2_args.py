'''
Python-এ *args ব্যবহার করা হয় এমন একটি ফাংশন ডিফাইন করার জন্য, যা অজানা সংখ্যক পজিশনাল আর্গুমেন্ট গ্রহণ করতে পারে। এটি তখনই উপকারী হয় যখন আপনি আগে থেকে নিশ্চিত নন যে ফাংশনে কতটি আর্গুমেন্ট পাস করা হবে।
=> কীভাবে কাজ করে?
1.*args-এর আগে থাকা * Python-কে বলে যে, যত অতিরিক্ত পজিশনাল আর্গুমেন্ট থাকবে, সবগুলোকে একটি tuple-এ সংরক্ষণ করতে হবে।
2.args শুধু একটি নাম; আপনি অন্য নামও ব্যবহার করতে পারেন (যেমন, *numbers), কিন্তু এর আগে অবশ্যই * থাকতে হবে।
'''
def hello_everyone (*names):
    for name in names:
        print(f"Hello {name}")

hello_everyone('Anis','Arif','Shariar','Muhi')

'''
=> কী ঘটছে:
1.hello_everyone-এ 'Anis', 'Arif', 'Shariar', 'Muhi' পাস করা হয়েছে।
2.*names এর মাধ্যমে এগুলো একটি tuple (('Anis', 'Arif', 'Shariar', 'Muhi')) হিসেবে ফাংশনের ভিতরে প্রবেশ করেছে।
3.লুপ প্রতিটি নাম প্রিন্ট করেছে।

'''
def calculation(n1,n2):
    sum = n1 + n2
    sub = n1 - n2 
    mul = n1 * n2
    return sum,sub,mul

ans = calculation(10,2)
print(ans)
'''
এই কোডটি একটি ফাংশন সংজ্ঞায়িত করে, যার নাম calculation, যা দুটি সংখ্যার যোগফল, বিয়োগফল, এবং গুণফল গণনা করে এবং একসঙ্গে রিটার্ন করে। এটি একটি tuple আকারে মানগুলো ফেরত দেয়।
=> কোড ব্যাখ্যা:
1.ফাংশনটি দুটি প্যারামিটার নেয়: n1 এবং n2।
2.তিনটি গণনা করা হয়:
    যোগফল: sum = n1 + n2
    বিয়োগফল: sub = n1 - n2
    গুণফল: mul = n1 * n2
3.শেষে তিনটি মান tuple আকারে রিটার্ন করা হয়: return sum, sub, mul.
'''